CREATE TABLE QNABBS(
	POST_NUMBER NUMBER(10) PRIMARY KEY,
	EMAIL VARCHAR2(50),
	TITLE VARCHAR2(200) NOT NULL,
	CONTENT VARCHAR2(4000) NOT NULL,
	STEP NUMBER(5) NOT NULL,
	REF NUMBER(5) NOT NULL,
	DEL NUMBER(5) NOT NULL,
	WDATE DATE NOT NULL,
	CONSTRAINT FK_EMAIL FOREIGN KEY(EMAIL)
    REFERENCES MEMBER(EMAIL)
);

CREATE TABLE MAINBBS(
	POST_NUMBER NUMBER(10) PRIMARY KEY,
	EMAIL VARCHAR2(50),
	TITLE VARCHAR2(200) NOT NULL,
	CONTENT VARCHAR2(4000) NOT NULL,
	LOCATION VARCHAR2(50) NOT NULL,
	AUTH_CHECK NUMBER(5) NOT NULL,
	IMGNAME VARCHAR2(50) NOT NULL,
	ORIGINFILENAME VARCHAR2(50) NOT NULL,
	CONSTRAINT FK_MAIN_EMAIL FOREIGN KEY(EMAIL)
    REFERENCES MEMBER(EMAIL)
	
);

