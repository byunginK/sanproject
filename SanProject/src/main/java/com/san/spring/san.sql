CREATE TABLE MAINLIKE(
	LIKE_NUMBER NUMBER(5) PRIMARY KEY,
	EMAIL VARCHAR2(50),
	MAIN_POST_NUMBER NUMBER(10),
	LIKE_COUNT NUMBER(10) NOT NULL,
	CONSTRAINT FK_LIKE_EMAIL FOREIGN KEY(EMAIL)
    REFERENCES MEMBER(EMAIL),
    CONSTRAINT FK_MAIN_POST2 FOREIGN KEY(MAIN_POST_NUMBER)
    REFERENCES MAINBBS(POST_NUMBER)
);
	

CREATE TABLE MAINCOMMENT(
	POST_NUMBER NUMBER(10) PRIMARY KEY,
