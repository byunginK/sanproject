	MAIN_POST_NUMBER NUMBER(10),
	CONTENT VARCHAR2(4000) NOT NULL,
	STEP NUMBER(5) NOT NULL,
	REF NUMBER(5) NOT NULL,
	DEL NUMBER(5) NOT NULL,
	WDATE DATE NOT NULL,
	CONSTRAINT FK_COMMENT_EMAIL FOREIGN KEY(EMAIL)
    REFERENCES MEMBER(EMAIL),
    CONSTRAINT FK_MAIN_POST FOREIGN KEY(MAIN_POST_NUMBER)
    REFERENCES MAINBBS(POST_NUMBER)
);

